package com.class_ic.app.dto;

public class memberdto {
	private String attendNo;
	private String email;
	private String attendDate;
	private String time;
	private String attendState;
	private String classCode;
	public String getAttendNo() {
		return attendNo;
	}
	public void setAttendNo(String attendNo) {
		this.attendNo = attendNo;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getAttendDate() {
		return attendDate;
	}
	public void setAttendDate(String attendDate) {
		this.attendDate = attendDate;
	}

	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getAttendState() {
		return attendState;
	}
	public void setAttendState(String attendState) {
		this.attendState = attendState;
	}
	public String getClassCode() {
		return classCode;
	}
	public void setClassCode(String classCode) {
		this.classCode = classCode;
	}

	

}
